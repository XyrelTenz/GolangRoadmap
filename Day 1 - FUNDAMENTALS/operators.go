package main

import "fmt"

func main() {
    a, b := 10, 5

    // Arithmetic operators
    fmt.Println("a + b =", a+b)
    fmt.Println("a - b =", a-b)
    fmt.Println("a * b =", a*b)

    // Comparison operators
    fmt.Println("a > b is", a > b)

    // Logical operators
    fmt.Println("true && false is", true && false)
}
