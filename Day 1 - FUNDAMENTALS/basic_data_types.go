package main

import "fmt"

func main() {
    // Integer
    var age int = 25

    // Float
    var score float64 = 95.5

    // String
    var message string = "Welcome to Go!"

    // Boolean
    var isActive bool = true

    fmt.Println("Age:", age)
    fmt.Println("Score:", score)
    fmt.Println("Message:", message)
    fmt.Println("Is Active?:", isActive)
}
